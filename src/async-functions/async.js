export async function getRouteResults(locations) {
  const routeParams = new URLSearchParams({
    orig: locations[0],
    dest: locations[1],
  });
  const response = await fetch(`${endpoint}routes/?` + routeParams, {
    method: "GET",
    mode: "cors",
    cache: "no-cache",
    credentials: "same-origin",
  });
  return response.json();
}

